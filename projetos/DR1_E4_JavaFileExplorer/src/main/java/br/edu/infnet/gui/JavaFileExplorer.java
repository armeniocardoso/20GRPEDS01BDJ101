    private void txtDiretorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtDiretorioActionPerformed
        
        //----------------------------------------------------------------------
        //Limpar a tabela
        DefaultTableModel defaultTableModel = (DefaultTableModel) tblArquivos.getModel();
        int numeroLinhas = defaultTableModel.getRowCount();
        for(int i = numeroLinhas - 1; i >= 0; i--) {
            
            defaultTableModel.removeRow(i);
        }
        //----------------------------------------------------------------------
        //Preencher a tabela
        File dir = new File(txtDiretorio.getText());
        if(dir.isDirectory()) {
            
            File [] arquivos = dir.listFiles();
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy kk:mm");
            for (File arquivo : arquivos) {
                
                String tipo = arquivo.isFile() ? "Arquivo" : "Diret칩rio";
                String modificado = sdf.format(new Date(arquivo.lastModified()));
                Object [] linhaDaTabela = {
                    arquivo.getName(),
                    tipo,
                    arquivo.length(),
                    modificado,
                    arquivo.canRead(),
                    arquivo.canWrite()                    
                };
                defaultTableModel.addRow(linhaDaTabela);
            }
        } else {
            
            JOptionPane.showMessageDialog(this, 
                    "Diret칩rio Inv치lido", 
                    "ERRO", 
                    JOptionPane.ERROR_MESSAGE);            
        }
    }//GEN-LAST:event_txtDiretorioActionPerformed

